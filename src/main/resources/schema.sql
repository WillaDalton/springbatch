CREATE TABLE IF NOT EXISTS PERSON_BATCH (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERSON_NUMBER VARCHAR(64) NOT NULL,
    NOM VARCHAR(128) NOT NULL,
    PRENOM VARCHAR(128) NOT NULL,
    CODE_ENTREPRISE CHAR(6) NOT NULL,
    DATE_ENTREE DATE NOT NULL,
    DATE_SORTIE DATE
);

CREATE INDEX IF NOT EXISTS IDX_PERSON_BATCH_ACTIVE
    ON PERSON_BATCH (PERSON_NUMBER, NOM, PRENOM, CODE_ENTREPRISE, DATE_SORTIE);

CREATE TABLE IF NOT EXISTS EXEC_BATCH (
    EXECUTION_NUMBER BIGINT PRIMARY KEY,
    EXECUTION_DATE TIMESTAMP NOT NULL,
    STATUS VARCHAR(32) NOT NULL
);
